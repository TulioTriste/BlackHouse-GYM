    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        // TODO add your handling code here:
        if (rutTextField.getText().isEmpty()) {
            resultadoLabel.setText("Debes ingresar un rut.");
        } else {
            try {
                Lobby.st.executeUpdate("delete from miembro where rut = '" + rutTextField.getText() + "';");
                resultadoLabel.setText("Se ha eliminado al usuario correctamente.");
            } catch (SQLException e) {
                System.out.println(e.getMessage());
                resultadoLabel.setText("Hubo un error intentando enviar la se√±al.");
            }
        }
    }//GEN-LAST:event_modificarActionPerformed

