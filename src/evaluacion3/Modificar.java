    private void modificar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificar2ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String sql = "DELETE FROM miembro WHERE rut = '" + rutTextField.getText() + "'";

        // Ejecutar la sentencia
        try {
            Lobby.st.executeUpdate(sql);
        } catch(SQLException e) {
            System.out.println(e.getMessage());
        }
        
        if (rutTextField.getText().isEmpty() || nombreTextField.getText().isEmpty() || appTextField.getText().isEmpty()
                || apmTextField.getText().isEmpty() || cuotaMensualTextField.getText().isEmpty()) {
            resultLabel.setText("Hay campos vacios, procura llenar todos los campos con información.");
        } else {
            String sql1 = "insert into miembro values(Null, '" + rutTextField.getText() + "', '"
                    + nombreTextField.getText() + "', '"
                    + appTextField.getText() + "', '"
                    + apmTextField.getText() + "', '"
                    + tallerComboBox.getSelectedItem().toString()+ "', "
                    + diasInsComboBox.getSelectedItem().toString() + ", "
                    + cuotaMensualTextField.getText()
                    + ");";
            
            try {
                Lobby.st.executeUpdate(sql1);
                resultLabel.setText("Usuario Actualizado correctamente!");
            } catch (SQLException e) {
                System.out.println(e.getMessage());
                resultLabel.setText("Ha ocurrido un error, revisa la consola.");
            }
        }
    }//GEN-LAST:event_modificar2ActionPerformed

