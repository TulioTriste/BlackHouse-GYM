    private void cargarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarButtonActionPerformed
        // TODO add your handling code here:(con un barra update)
        if (!rutTextField.getText().isEmpty()) {
            try {
                String sql = "SELECT * FROM miembro WHERE rut = '" + rutTextField.getText() + "'";

                ResultSet result = Lobby.st.executeQuery(sql);

                if (result.next()) {
                    // Obtener datos del ResultSet
                    Object rut = result.getObject(2);
                    Object nombre = result.getObject(3);
                    Object apPaterno = result.getObject(4);
                    Object apMaterno = result.getObject(5);
                    Object taller = result.getObject(6);
                    Object diasInscritosResult = result.getObject(7);
                    Object cuotaMensual = result.getObject(8);

                    nombreTextField.setText(nombre.toString());
                    appTextField.setText(apPaterno.toString());
                    apmTextField.setText(apMaterno.toString());
                    switch (taller.toString()) {
                        case "Karate":
                            tallerComboBox.setSelectedIndex(0);
                            break;
                        case "Judo":
                            tallerComboBox.setSelectedIndex(1);
                            break;
                        case "Kung-fu":
                            tallerComboBox.setSelectedIndex(2);
                            break;
                        case "Boxeo":
                            tallerComboBox.setSelectedIndex(3);
                            break;
                        case "Jiu-jitsu":
                            tallerComboBox.setSelectedIndex(4);
                            break;
                        case "Capoeira":
                            tallerComboBox.setSelectedIndex(5);
                            break;
                        case "Muay Thai":
                            tallerComboBox.setSelectedIndex(6);
                            break;
                        case "Krav Maga":
                            tallerComboBox.setSelectedIndex(7);
                            break;
                        case "Kendo":
                            tallerComboBox.setSelectedIndex(8);
                            break;
                        case "Aikido":
                            tallerComboBox.setSelectedIndex(9);
                            break;
                        case "Taekwondo":
                            tallerComboBox.setSelectedIndex(10);
                            break;
                        case "Esgrima":
                            tallerComboBox.setSelectedIndex(11);
                            break;
                        default:
                            break;
                    }
                    switch (diasInscritosResult.toString()) {
                        case "30":
                            diasInsComboBox.setSelectedIndex(0);
                            break;
                        case "90":
                            diasInsComboBox.setSelectedIndex(1);
                            break;
                        case "180":
                            diasInsComboBox.setSelectedIndex(2);
                            break;
                        default:
                            break;
                    }
                    cuotaMensualTextField.setText(cuotaMensual.toString());
                }
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_cargarButtonActionPerformed

